    def _parse_json_with_cleaning(self, json_str: str) -> Dict[str, Any]:
        """
        Parse JSON with aggressive cleaning to handle common LLM errors.
        
        Args:
            json_str: JSON string to parse
            
        Returns:
            Parsed JSON object
        """
        # Step 1: Remove trailing commas before closing braces/brackets
        cleaned = re.sub(r',(\s*[}\]])', r'\1', json_str)
        
        # Step 2: Try parsing
        try:
            return json.loads(cleaned)
        except json.JSONDecodeError as e:
            logger.debug(f"First parse attempt failed: {e}, trying more aggressive cleaning")
        
        # Step 3: More aggressive cleaning
        # Remove comments (// and /* */)
        cleaned = re.sub(r'//.*?$', '', cleaned, flags=re.MULTILINE)
        cleaned = re.sub(r'/\*.*?\*/', '', cleaned, flags=re.DOTALL)
        
        # Fix common quote issues
        # Replace smart quotes with regular quotes
        cleaned = cleaned.replace('"', '"').replace('"', '"')
        cleaned = cleaned.replace(''', "'").replace(''', "'")
        
        # Fix missing commas between properties (heuristic)
        # This is risky but can help with some cases
        cleaned = re.sub(r'"\s*\n\s*"', '",\n"', cleaned)
        
        # Remove trailing commas again after fixes
        cleaned = re.sub(r',(\s*[}\]])', r'\1', cleaned)
        
        # Step 4: Try parsing again
        try:
            return json.loads(cleaned)
        except json.JSONDecodeError as e:
            logger.debug(f"Second parse attempt failed: {e}, trying to extract valid JSON")
        
        # Step 5: Last resort - try to extract the largest valid JSON object
        # Find all potential JSON objects and try to parse them
        brace_depth = 0
        start_idx = -1
        
        for i, char in enumerate(cleaned):
            if char == '{':
                if brace_depth == 0:
                    start_idx = i
                brace_depth += 1
            elif char == '}':
                brace_depth -= 1
                if brace_depth == 0 and start_idx != -1:
                    # Found a complete object
                    potential_json = cleaned[start_idx:i+1]
                    try:
                        return json.loads(potential_json)
                    except json.JSONDecodeError:
                        continue
        
        # If all else fails, raise the original error
        raise json.JSONDecodeError(f"Could not parse JSON after cleaning", json_str, 0)
    
    def _apply_rejection_avoidance(
        self,
        input_event: Dict[str, Any],
        rejection_analysis: Dict[str, Any],
        analysis_result: AnalysisResult
    ) -> Dict[str, Any]:
        """Apply rejection avoidance based on historical rejections."""
        if not rejection_analysis or not rejection_analysis.get('reason_counts'):
            return input_event
        
        event = input_event.copy()
        top_reasons = rejection_analysis.get('top_reasons', [])
        
        for reason in top_reasons:
            if reason == 'missing_auth_headers':
                # Ensure auth headers are present
                if 'input_event' in event and 'headers' in event['input_event']:
                    event['input_event']['headers']['Authorization'] = 'Bearer <TOKEN>'
                    event['input_event']['headers']['X-API-Key'] = '<API_KEY>'
                    logger.debug("Added auth headers to avoid rejection")
            
            elif reason == 'wrong_status_code':
                # Use realistic status codes
                if 'expected_output' in event and 'statusCode' in event['expected_output']:
                    # Keep the status code from analysis
                    logger.debug("Using analyzed status code to avoid rejection")
            
            elif reason == 'unrealistic_data':
                # Use more realistic test data
                if 'input_event' in event and 'body' in event['input_event']:
                    try:
                        body = json.loads(event['input_event']['body']) if isinstance(event['input_event']['body'], str) else event['input_event']['body']
                        # Add realistic fields
                        if isinstance(body, dict):
                            body['timestamp'] = self._get_timestamp()
                            body['requestId'] = self._generate_id()
                            event['input_event']['body'] = json.dumps(body) if isinstance(event['input_event']['body'], str) else body
                        logger.debug("Enhanced data realism to avoid rejection")
                    except (json.JSONDecodeError, TypeError, KeyError) as e:
                        logger.debug(f"Could not enhance data realism: {e}")
            
            elif reason == 'incorrect_event_source':
                # Ensure event source matches function type
                logger.debug(f"Event source validated against function type")
            
            elif reason == 'missing_required_fields':
                # Add commonly required fields
                if 'input_event' in event:
                    if 'requestContext' not in event['input_event']:
                        event['input_event']['requestContext'] = {
                            'requestId': self._generate_id(),
                            'stage': 'test'
                        }
                    logger.debug("Added required fields to avoid rejection")
        
        return event
    
    def _select_pattern(self, patterns: List[Dict[str, Any]], test_type: str) -> Optional[Dict[str, Any]]:
        """Select a relevant pattern from accepted patterns."""
        if not patterns:
            return None
        
        # Filter patterns by test type
        relevant_patterns = [p for p in patterns if p.get('test_type') == test_type]
        
        if not relevant_patterns:
            return None
        
        # Select pattern with highest success rate
        return max(relevant_patterns, key=lambda p: p.get('success_rate', 0.0))
    
