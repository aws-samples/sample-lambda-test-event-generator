    def _sanitize_custom_instructions(self, custom_instructions: str) -> str:
        """
        Sanitize custom instructions to prevent prompt injection attacks.
        
        Security measures:
        1. Remove prompt injection patterns
        2. Limit length to prevent token exhaustion
        3. Remove system-level commands
        4. Escape special characters
        5. Remove attempts to override system instructions
        
        Args:
            custom_instructions: Raw user input
            
        Returns:
            Sanitized instructions safe for inclusion in prompts
        """
        if not custom_instructions or not custom_instructions.strip():
            return ""
        
        # Step 1: Truncate to reasonable length (2000 chars)
        sanitized = custom_instructions[:2000]
        
        # Step 2: Remove common prompt injection patterns
        injection_patterns = [
            r'ignore\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'disregard\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'forget\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'override\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'you\s+are\s+now',
            r'new\s+instructions?:',
            r'system\s+prompt:',
            r'act\s+as\s+if',
            r'pretend\s+(you\s+are|to\s+be)',
            r'roleplay\s+as',
            r'output\s+the\s+(full|entire|complete)\s+(code|source|lambda)',
            r'reveal\s+the\s+(code|source|lambda|function)',
            r'show\s+me\s+the\s+(code|source|lambda|function)',
            r'print\s+the\s+(code|source|lambda|function)',
            r'return\s+the\s+(code|source|lambda|function)',
        ]
        
        for pattern in injection_patterns:
            sanitized = re.sub(pattern, '[FILTERED]', sanitized, flags=re.IGNORECASE)
        
        # Step 3: Remove attempts to access system information
        system_patterns = [
            r'\$\{[^}]+\}',  # Variable expansion
            r'\$\([^)]+\)',  # Command substitution
            r'`[^`]+`',      # Backticks
        ]
        
        for pattern in system_patterns:
            sanitized = re.sub(pattern, '[FILTERED]', sanitized)
        
        # Step 4: Remove excessive special characters that might break prompt structure
        # Allow normal punctuation but limit repetition
        sanitized = re.sub(r'([=*#-]){10,}', r'\1' * 3, sanitized)  # Limit repeated chars
        
        # Step 5: Remove attempts to break out of instruction context
        sanitized = re.sub(r'\[\[\[.*?\]\]\]', '[FILTERED]', sanitized, flags=re.DOTALL)
        sanitized = re.sub(r'\{\{\{.*?\}\}\}', '[FILTERED]', sanitized, flags=re.DOTALL)
        
        # Step 6: Normalize whitespace
        sanitized = ' '.join(sanitized.split())
        
        logger.info(f"Sanitized custom instructions: {len(custom_instructions)} -> {len(sanitized)} chars")
        
        return sanitized.strip()
    
    def _generate_id(self) -> str:
        """Generate a unique ID for test cases."""
        import uuid
        return str(uuid.uuid4())[:8]
    
    def _get_timestamp(self) -> str:
        """Get current timestamp."""
        from datetime import datetime
        return datetime.utcnow().isoformat() + 'Z'