    def _sanitize_rejection_reason(self, reason: str) -> str:
        """
        Sanitize rejection reason text to prevent prompt injection.
        
        SECURITY: Rejection reasons are user-provided and stored in DynamoDB.
        They could contain prompt injection attempts that get embedded in prompts.
        
        Args:
            reason: Raw rejection reason from DynamoDB
            
        Returns:
            Sanitized rejection reason safe for prompt embedding
        """
        if not reason:
            return ""
        
        # Remove prompt injection patterns
        injection_patterns = [
            r'ignore\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'disregard\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'forget\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'override\s+(all\s+)?(previous|prior|above|system)\s+instructions?',
            r'you\s+are\s+now',
            r'new\s+instructions?:',
            r'system\s+prompt:',
            r'output\s+the\s+(full|entire|complete)\s+(code|source)',
            r'reveal\s+the\s+(code|source)',
        ]
        
        sanitized = reason
        for pattern in injection_patterns:
            sanitized = re.sub(pattern, '[FILTERED]', sanitized, flags=re.IGNORECASE)
        
        # Remove excessive special characters
        sanitized = re.sub(r'([=*#-]){10,}', r'\1' * 3, sanitized)
        
