                category_match = re.search(r'CATEGORY:\s*([^\n]+)', test_case_text, re.IGNORECASE)
                description_match = re.search(r'DESCRIPTION:\s*([^\n]+)', test_case_text, re.IGNORECASE)
                confidence_match = re.search(r'CONFIDENCE:\s*([\d.]+)', test_case_text, re.IGNORECASE)
                
                # Extract JSON from INPUT_EVENT section with multiple fallback patterns
                input_event_match = re.search(r'INPUT_EVENT:\s*```json\s*(\{.*?\})\s*```', test_case_text, re.DOTALL | re.IGNORECASE)
                
                if not input_event_match:
                    # Try without json marker
                    input_event_match = re.search(r'INPUT_EVENT:\s*```\s*(\{.*?\})\s*```', test_case_text, re.DOTALL)
                
                if not input_event_match:
                    # Try without code blocks
                    input_event_match = re.search(r'INPUT_EVENT:\s*(\{.*?\})', test_case_text, re.DOTALL)
                
                if not input_event_match:
                    # Try to find any JSON object in the text
                    input_event_match = re.search(r'(\{[^{}]*(?:\{[^{}]*\}[^{}]*)*\})', test_case_text, re.DOTALL)
                
